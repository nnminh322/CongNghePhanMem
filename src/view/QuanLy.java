    public HoKhau getHoKhau(int selected) {

        String soHoKhau = this.jTable_HoKhau.getValueAt(selected, 0).toString();
        String hoTenChuHo = this.jTable_HoKhau.getValueAt(selected, 1).toString();
        int soNha = Integer.parseInt(this.jTable_HoKhau.getValueAt(selected, 2).toString());
        String duongPho = this.jTable_HoKhau.getValueAt(selected, 3).toString();
        String phuongXa = this.jTable_HoKhau.getValueAt(selected, 4).toString();
        String quanHuyen = this.jTable_HoKhau.getValueAt(selected, 5).toString();

        return new HoKhau(soHoKhau, hoTenChuHo, soNha, duongPho, phuongXa, quanHuyen);
    }

