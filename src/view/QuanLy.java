    public void hienthiNhanKhau() {
        ArrayList<NhanKhau> listNhanKhau = this.quanLyController.hienthiNhanKhau();
        DefaultTableModel model = (DefaultTableModel) jTable_NhanKhau.getModel();
        model.setRowCount(0);
        for (NhanKhau nhanKhau : listNhanKhau) {
            Object[] row = {
                nhanKhau.getHoTen(),
                nhanKhau.getBiDanh(),
                nhanKhau.getNgaySinh(),
                nhanKhau.getNguyenQuan(),
                nhanKhau.getDanToc(),
                nhanKhau.getNgheNghiep(),
                nhanKhau.getCCCD(),
                nhanKhau.getGioiTinh(),
                nhanKhau.getSoHoKhau(),};
            model.addRow(row);
        }
    }

